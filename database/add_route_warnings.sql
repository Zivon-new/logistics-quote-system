-- ============================================================

CREATE TABLE IF NOT EXISTS `route_warnings` (
    `预警ID`      INT UNSIGNED    NOT NULL AUTO_INCREMENT,
    `国家代码`    CHAR(2)         NOT NULL        COMMENT 'ISO 3166-1 alpha-2，如 YE/IQ/UA',
    `国家中文名`  VARCHAR(60)     NOT NULL,
    `目的地关键词` VARCHAR(100)   NOT NULL        COMMENT '匹配 routes.目的地 的关键词，如 也门/Yemen',
    `风险类型`    VARCHAR(40)     NOT NULL        COMMENT '战争冲突/制裁封锁/港口罢工/海盗威胁/自然灾害/政治动荡',
    `风险等级`    TINYINT         NOT NULL        COMMENT '1=低 2=中 3=高',
    `预警标题`    VARCHAR(100)    NOT NULL,
    `预警详情`    TEXT            DEFAULT NULL,
    `生效日期`    DATE            NOT NULL,
    `是否有效`    TINYINT(1)      NOT NULL DEFAULT 1,
    `创建时间`    DATETIME        DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (`预警ID`),
    INDEX `idx_country` (`国家代码`),
    INDEX `idx_keyword` (`目的地关键词`),
    INDEX `idx_valid`   (`是否有效`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='航线风险预警';


